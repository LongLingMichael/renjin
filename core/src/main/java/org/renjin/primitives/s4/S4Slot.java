package org.renjin.primitives.s4;


public class S4Slot {
  private String name;
  private S4Class slotClass;

  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }

  public S4Class getSlotClass() {
    return slotClass;
  }

  public void setSlotClass(S4Class slotClass) {
    this.slotClass = slotClass;
  }
}
